        //
        try{
            $request->validate([
                'content'=>'required',
                'content'=> 'required',
            ]);
     
            $post = new Post([
                'title' => $request->get('content'),
                'content'=> $request->get('content'),
            ]);
            // print_r(json_decode($post));exit;
     
            $post->save();

        }catch(\Exception $e){
            return [
            'msg' => $e->getMessage(), 
            'file' => $e->getFile(), 
            'line' => $e->getLine(), 
            'trace' => $e->getTrace()];
        }
        
    }

